export function transliterateToTelugu(text) {
    if (!text) return '';
    
    const input = text.toLowerCase();
    let result = '';
    let i = 0;
    
    while (i < input.length) {
        let matched = false;
        
        // Try longest matches first (up to 12 characters)
        for (let len = Math.min(12, input.length - i); len >= 1; len--) {
            const substr = input.substring(i, i + len);
            
            if (TELUGU_TRANSLITERATION_MAP[substr]) {
                result += TELUGU_TRANSLITERATION_MAP[substr];
                i += len;
                matched = true;
                break;
            }
        }
        
        // If no match found, keep the character as is
        if (!matched) {
            result += input[i];
            i++;
        }
    }
    
    return result;
}